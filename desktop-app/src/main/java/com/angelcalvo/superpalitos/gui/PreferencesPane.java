  private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String name1 = j1NameTF.getText();
    String name2 = j2NameTF.getText();
    try {
      int n3 = Integer.parseInt(portTF.getText());
      if(name1.length() > 0  && name2.length() > 0 && n3 > 1024 && n3 < 65535) {
        sp.setJ1Name(name1);
   //     m.setJ1Color(j1ColorCB.getSelectedColor());
        sp.setJ2Name(name2);
     //   m.setJ2Color(j2ColorCB.getSelectedColor());
        sp.setPuerto(n3);
        sp.setAnim(animsCheck.isSelected());
        sp.setSound(soundCheck.isSelected());
        
        if(lnfCB.getSelectedIndex() != currentLaF) {
          currentLaF = lnfCB.getSelectedIndex();
          UIManager.setLookAndFeel(lafs[currentLaF].getClassName());
          sp.changeLaF();
        }
      }
    } catch(NumberFormatException ex) {
      portTF.setText("0");
    } catch (ClassNotFoundException ex) {
      ex.printStackTrace();
    } catch (UnsupportedLookAndFeelException ex) {
      ex.printStackTrace();
    } catch (IllegalAccessException ex) {
      ex.printStackTrace();
    } catch (InstantiationException ex) {
      ex.printStackTrace();
    }
  }//GEN-LAST:event_jButton1ActionPerformed

