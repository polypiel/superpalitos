  private void initLookAndFeels() {
    lafs = UIManager.getInstalledLookAndFeels();
    lookAndFeelTags = new String[lafs.length];
    
    for(int i = 0; i < lafs.length; i++ ) {
      lookAndFeelTags[i] = lafs[i].getName();
      if(UIManager.getLookAndFeel().getName().equals(lafs[i].getName())) {
        currentLaF = i;
      }
    }
  }
    
  

  
